package com.taisheng.now.bussiness.watch.bean.result;

public class BushuBean {
    public String clientId;
    public String createTime;
    public String electricQuantity;
    public String id;
    public String rolloverTimes;
    public String stepNum;

    public String getClientId() {
        return clientId;
    }

    public void setClientId(String clientId) {
        this.clientId = clientId;
    }

    public String getCreateTime() {
        return createTime;
    }

    public void setCreateTime(String createTime) {
        this.createTime = createTime;
    }

    public String getElectricQuantity() {
        return electricQuantity;
    }

    public void setElectricQuantity(String electricQuantity) {
        this.electricQuantity = electricQuantity;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getRolloverTimes() {
        return rolloverTimes;
    }

    public void setRolloverTimes(String rolloverTimes) {
        this.rolloverTimes = rolloverTimes;
    }

    public String getStepNum() {
        return stepNum;
    }

    public void setStepNum(String stepNum) {
        this.stepNum = stepNum;
    }
}
